#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;
	int j;
	int k;

	i = 0;
	j = 0;
	k = -1;
	while (s[j])
		j++;
	while (i <= j)
	{
		if (s[i] == c)
			k = i;
		i++;
	}
	return (k == -1 ? (NULL) : ((char *)s + k));
}
